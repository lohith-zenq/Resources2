package Task4;

public class Arithmeticop
{
	static int a=8,b=5,c;
	
	public static void add()
	{
		c=a+b;
		System.out.println(c);
	}
	public static void sub()
	{
		c=a-b;
		System.out.println(c);
	}
	public static void mul()
	{
		c=a*b;
		System.out.println(c);
	}
	public static void div()
	{
		c=a/b;
		System.out.println(c);
	}
	
	public static void main(String[] args) 
       {
       add();
       sub();
       mul();
       div();
	}

}


